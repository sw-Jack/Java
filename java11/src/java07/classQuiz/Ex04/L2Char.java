package java07.classQuiz.Ex04;

public class L2Char {
	private String tribe;  // 종족
	private String job;  // 직업
	private String hair;  // 머리 스타일
	private String nickName;  // 캐릭터명
	private String jobExplain;  // 직업 설명
	
	public String getTribe() {
		return tribe;
	}
	public void setTribe(String tribe) {
		this.tribe = tribe;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	
	public String getHair() {
		return hair;
	}
	public void setHair(String hair) {
		this.hair = hair;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getJobExplain() {
		return jobExplain;
	}
	public void setJobExplain(String jobExplain) {
		this.jobExplain = jobExplain;
	}
}
