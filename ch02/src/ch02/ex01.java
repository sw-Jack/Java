package ch02;

public class ex01 {
	public static void main(String[] args) {
		byte a = 10; // 1byte    -128 ~ 127
		short b = 1000; // 2byte    -2^15 ~ 2^15-1  
		int c = 42000; // 4byte    -2^31 ~ 2^31-1, 기본 자료형
		long d = 1000L; // 8byte    -2^63~ 2^163-1
		char e = 'A'; // 2byte    0 ~ 2^16-1
		float f = 10.23F; // 4byte
		double g = 10.23; // 8byte, 기본 자료형
		boolean h = true; // 1byte     true or false
		String i = "자바 공부"; // 문자열 자료형
		
	
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
